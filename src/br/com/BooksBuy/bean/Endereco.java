package br.com.BooksBuy.bean;

public class Endereco{
	
	private Integer idEndereco;
	private String logradouroEndereco;
	private String numeroResidenciaEndereco;
	private String cepEndereco;
	private String bairroEndereco;
	private String complementoEndereco;
	private String municipioEndereco;
	private String estadoEndereco;
	private String cpfClienteTbCliente;
	
	public Integer getIdEndereco() {
		return idEndereco;
	}
	public void setIdEndereco(Integer idEndereco) {
		this.idEndereco = idEndereco;
	}
	public String getLogradouroEndereco() {
		return logradouroEndereco;
	}
	public void setLogradouroEndereco(String logradouroEndereco) {
		this.logradouroEndereco = logradouroEndereco;
	}
	public String getNumeroResidenciaEndereco() {
		return numeroResidenciaEndereco;
	}
	public void setNumeroResidenciaEndereco(String numeroResidenciaEndereco) {
		this.numeroResidenciaEndereco = numeroResidenciaEndereco;
	}
	public String getCepEndereco() {
		return cepEndereco;
	}
	public void setCepEndereco(String cepEndereco) {
		this.cepEndereco = cepEndereco;
	}
	public String getBairroEndereco() {
		return bairroEndereco;
	}
	public void setBairroEndereco(String bairroEndereco) {
		this.bairroEndereco = bairroEndereco;
	}
	public String getComplementoEndereco() {
		return complementoEndereco;
	}
	public void setComplementoEndereco(String complementoEndereco) {
		this.complementoEndereco = complementoEndereco;
	}
	public String getMunicipioEndereco() {
		return municipioEndereco;
	}
	public void setMunicipioEndereco(String municipioEndereco) {
		this.municipioEndereco = municipioEndereco;
	}
	public String getEstadoEndereco() {
		return estadoEndereco;
	}
	public void setEstadoEndereco(String estadoEndereco) {
		this.estadoEndereco = estadoEndereco;
	}
	public String getCpfClienteTbCliente() {
		return cpfClienteTbCliente;
	}
	public void setCpfClienteTbCliente(String cpfClienteTbCliente) {
		this.cpfClienteTbCliente = cpfClienteTbCliente;
	}
}
